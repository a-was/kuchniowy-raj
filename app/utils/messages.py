def info_message(text):
    return {
        'type': 'info',
        'text': text,
    }


def success_message(text):
    return {
        'type': 'success',
        'text': text,
    }


def warning_message(text):
    return {
        'type': 'warning',
        'text': text,
    }


def error_message(text):
    return {
        'type': 'error',
        'text': text,
    }
